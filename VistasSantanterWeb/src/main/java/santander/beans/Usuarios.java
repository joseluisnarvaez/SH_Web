package santander.beans;

public class Usuarios {
	
	String user,password,sponsor,nombre_sponsor;
	int Rut;

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getSponsor() {
		return sponsor;
	}

	public void setSponsor(String sponsor) {
		this.sponsor = sponsor;
	}

	public int getRut() {
		return Rut;
	}

	public void setRut(int rut) {
		Rut = rut;
	}

	public String getNombre_sponsor() {
		return nombre_sponsor;
	}

	public void setNombre_sponsor(String nombre_sponsor) {
		this.nombre_sponsor = nombre_sponsor;
	}
	
	

}
